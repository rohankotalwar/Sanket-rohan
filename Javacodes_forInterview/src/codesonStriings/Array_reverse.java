package codesonStriings;

public class Array_reverse {

	
	public static void main(String[]args) {
	
		  
	

			int number=1234567890;
				
			
			int Digit;
			
			while(number>0)
			{	
				Digit=number%10;
				number=number/10;
				System.out.print(Digit);
			}
		}	  
		  
		  
	  

}