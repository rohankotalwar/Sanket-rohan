package codesonStriings;



public class Factorial {
	

	public static void FactorialNum (int Num) {
		
		int Fact=1 ;
		
		
		for(int i=Num ; i>0 ;i--) {
			
			     Fact = Fact*i ;
			     
		}
		
		System.out.println("Factorial number of "+Num+" is:" +Fact);
		
	}
	
	public static void main(String[]args) {
		
		FactorialNum(4);
		

	
	
	
	
	
	
	
	
	}
}

	
	



	
	
	
	
		
		
		
		
		
	    
	    
	    
	    

