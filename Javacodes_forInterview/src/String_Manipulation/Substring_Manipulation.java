package String_Manipulation;

public class Substring_Manipulation {
	public static void main(String[]args) {
		
		String s ="SeleniumTesting";
		
		
		System.out.println(s.substring(2));  // it returns the String from Index 2
		                                     
		//Output:leniumTesting
		
		 
		System.out.println(s.substring(3, 7));  //startIndex : starting index is inclusive
		                                        
		//Output: eniu                          //endIndex : ending index is exclusive


	}

}
